=head1 NAME

App::Kaizendo - A tool for collaborative authoring of polyscopic documents


=head1 SYNOPSIS

    script/kaizendo_server.pl


=head1 DESCRIPTION

Kaizendo is a framework for discussing and improving texts that have
alternative versions ("aspects") of itself.

The application is currently under development, and isn't in a particularly
usable state yet. If you want to help please consult the README file!

If you want to learn more about the project, check out L<http://kaizendo.org/>.

